
public class BuddyInfo {

    // Instance Variable
    private final String name;
    private final String number;

    // Constructor Declaration
    public BuddyInfo (String name, String number) {
        this.name = name;
        this.number = number;
    }

    // Getter method
    public String getName() {
        return name;
    }

    public String getNumber() {
        return number;
    }

}
